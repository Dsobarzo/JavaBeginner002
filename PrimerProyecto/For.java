public class For
{
    public static void main(String[] args){
                                //0   1   2   3   4
        int [] arregloEnteros = {100,200,300,400,500};
        String [] arregloTextos = {"uno","dos","tres","cuatro","cinco"};
        
        for(int i = 0; i<= 4 ;i++){
            System.out.println(arregloEnteros[i]);
        }
        for(int i = 0; i<= 4 ;i++){
            System.out.println(arregloTextos[i]);
        }
        
        
    
    }
}
